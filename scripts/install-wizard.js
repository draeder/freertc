const dim     = (s) => `${c.dim}${s}${c.reset}`;
const green   = (s) => `${c.green}${s}${c.reset}`;
const yellow  = (s) => `${c.yellow}${s}${c.reset}`;
const cyan    = (s) => `${c.cyan}${s}${c.reset}`;
const red     = (s) => `${c.red}${s}${c.reset}`;

